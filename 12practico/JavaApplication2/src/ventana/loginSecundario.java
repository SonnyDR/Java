    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        registroPrincipal reg2 = new registroPrincipal();
        
        String correoLog = jTextFieldLogCorreo.getText();
        String contraLog = new String(jPasswordFieldPassLog.getPassword());
        nombreLog = reg2.nombre;
        apellidoLog = reg2.apellido;
        
        if (correoLog.equals(reg2.correo) && contraLog.equals(reg2.contrasenia)) {
            JOptionPane.showMessageDialog(null, "LOS DATOS INGRESADOS SON CORRECTOS!");
            dispose();   
        } else{
            JOptionPane.showMessageDialog(null, "ESOS DATOS NO SON CORRECTOS");
        }
        JOptionPane.showMessageDialog(null, "LOS DATOS INGRESADOS SON:"
                + "\n NOMBRE: " + nombreLog
                + "\n APELLIDO: " + apellidoLog
                 + "\n CORREO: " + correoLog 
                + "\n CONTRASEÃ‘A: " + contraLog);
    }//GEN-LAST:event_jButton1ActionPerformed

